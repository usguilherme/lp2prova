package prova02lp2;


public class Item {
    private String descricao;
    private int ano;

    public Item(String descricao, int ano) {
        this.descricao = descricao;
        this.ano = ano;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    public int getAno() {
        return ano;
    }

    public void setAno(int ano) {
        this.ano = ano;
    }







}

